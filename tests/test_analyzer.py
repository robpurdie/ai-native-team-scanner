    def test_create_90day_window_default(self):
        """Test creating 90-day window with default end date."""
        window = CommitAnalyzer.create_90day_window()
        
        # Should end approximately now
        now = datetime.now()
        assert abs((window.end_date - now).total_seconds()) < 60  # Within 1 minute
        
        # Should start 90 days before end
        expected_start = window.end_date - timedelta(days=90)
        assert abs((window.start_date - expected_start).total_seconds()) < 1

    def test_create_90day_window_custom_end(self):
        """Test creating 90-day window with custom end date."""
        end_date = datetime(2026, 3, 1)
        window = CommitAnalyzer.create_90day_window(end_date)
        
        assert window.end_date == end_date
        expected_start = end_date - timedelta(days=90)
        assert window.start_date == expected_start

    def test_analyze_commits_error_handling(self):
        """Test that errors are handled gracefully."""
        repo = Mock()
        repo.get_commits.side_effect = Exception("API error")
        
        window = ObservationWindow(
            start_date=datetime(2025, 12, 1),
            end_date=datetime(2026, 3, 1)
        )
        analyzer = CommitAnalyzer(repo, window)
        
        # Should return empty result, not raise exception
        result = analyzer.analyze_commits()
        
        assert result["total_commits"] == 0
        assert result["ai_assisted_commits"] == 0
        assert result["conventional_commits"] == 0
        assert len(result["contributors_with_ai"]) == 0
