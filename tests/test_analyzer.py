"""Tests for commit analyzer."""

from datetime import datetime, timedelta
from unittest.mock import Mock

import pytest

from scanner.analyzer import CommitAnalyzer
from scanner.models import ObservationWindow


class TestCommitAnalyzer:
    """Tests for CommitAnalyzer class."""

    def test_get_active_contributors(self):
        """Test getting active contributors from commits."""
        # Mock repository
        repo = Mock()
        
        # Mock commits with different authors
        commit1 = Mock()
        commit1.author.login = "alice"
        commit2 = Mock()
        commit2.author.login = "bob"
        commit3 = Mock()
        commit3.author.login = "alice"  # Duplicate
        
        repo.get_commits.return_value = [commit1, commit2, commit3]
        
        # Create analyzer
        window = ObservationWindow(
            start_date=datetime(2025, 12, 1),
            end_date=datetime(2026, 3, 1)
        )
        analyzer = CommitAnalyzer(repo, window)
        
        # Get contributors
        contributors = analyzer.get_active_contributors()
        
        # Verify
        assert len(contributors) == 2
        assert "alice" in contributors
        assert "bob" in contributors
        repo.get_commits.assert_called_once_with(
            since=window.start_date,
            until=window.end_date
        )

    def test_get_active_contributors_with_none_author(self):
        """Test handling commits with no author."""
        repo = Mock()
        
        # Mock commits, one without author
        commit1 = Mock()
        commit1.author.login = "alice"
        commit2 = Mock()
        commit2.author = None
        
        repo.get_commits.return_value = [commit1, commit2]
        
        window = ObservationWindow(
            start_date=datetime(2025, 12, 1),
            end_date=datetime(2026, 3, 1)
        )
        analyzer = CommitAnalyzer(repo, window)
        
        contributors = analyzer.get_active_contributors()
        
        assert len(contributors) == 1
        assert "alice" in contributors

    def test_analyze_commits_basic(self):
        """Test basic commit analysis."""
        repo = Mock()
        
        # Mock commits
        commit1 = Mock()
        commit1.commit.message = "feat: add feature with AI assistance"
        commit1.author.login = "alice"
        
        commit2 = Mock()
        commit2.commit.message = "fix: resolve bug"
        commit2.author.login = "bob"
        
        repo.get_commits.return_value = [commit1, commit2]
        
        window = ObservationWindow(
            start_date=datetime(2025, 12, 1),
            end_date=datetime(2026, 3, 1)
        )
        analyzer = CommitAnalyzer(repo, window)
        
        result = analyzer.analyze_commits()
        
